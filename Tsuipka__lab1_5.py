print("Програма, створена для конверутвання секунд з початку доби у години та хвилини")
seconds = int(input('Введіть час у секундах для конвертування його у години та хвилини = '))
print(f"Секунд = {seconds}")

hours = (seconds // 3600) % 24
minutes = (seconds // 60) % 60

print(f"Годин = {hours} :: Хвилин = {minutes}")
